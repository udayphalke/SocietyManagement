<h3 class="font-weight-bold" align="center"  style="color:#ff6666">Our commitment to information privacy</h3><br><BR><BR>



<div class="container" style="align" class="d">
<p>
  <ul>
    <li>
<h4 class="font-weight-bold">Information is only collected with consent:</h4> We only collects data with consent, and used for reasonable purposes (mainly to improve our services). We do not – and have never – shared &personal information of our users with any third-party. This extends also to profiles created for service providers during the sign-up process.</li><br>
  <li>
<h4 class="font-weight-bold">Personal information is always hidden with restricted access:</h4> We go to great lengths to ensure that personal information is hidden from view. Therefore, security guards can use the Softcare guard device only to punch in information submitted to approve or deny a request, but can’t access any information using the device as the app is loaded on to a proprietary device that is locked to our system. Furthermore, while society administrators do have access to a log of resident/guest information, all personal information here, too, is masked with an option to disable and control download rights at a community level.</li><br>
<li>
<h4 class="font-weight-bold">Access of information is always communicated:</h4> The Our app maintains a complete data access log for residents. This means, if a society administrator views any information relating to a resident, including the society visitor log, the resident will be informed that their data was viewed, by whom and for what reason.</li><br>
<li>
<h4 class="font-weight-bold">Visitor logs are periodically deleted:</h4> All entry and exit data is deleted from our systems after 180 days by default. All RWAs do have the option to reset this to 60 days or 120 days. Once deleted, even we will not be able to retrieve past data.</li><br>
<li>
<h4 class="font-weight-bold">Only reasonable information is collected:</h4> Our processes are set up to minimise the need for information. For example, at the society gate, visitors need only submit their name and share the flat they wish to visit, rather than a phone number.</li><br>
<li>
<h4 class="font-weight-bold">You have the right to be forgotten:</h4> Residents and visitors can have their personal information deleted from our systems, with the permission of respective RWAs as they are the owners of the records (note: this does not apply to financial records). Residents can do so via a click on the user app, while all others may do so by following the process mentioned here (note: all visitor information is deleted regardless after a maximum period of up to 180 days).</li><br>
<li>
<h4 class="font-weight-bold">We do not share or sell personal information:</h4> The agreement RWAs sign with us clearly states that we will never sell, trade or share any data. The information captured at the gate on the Our Guard App is the property of respective RWAs and only the Management Committee can access it, is only a custodian of the information. For our partnerships with third-party service providers, explicit user consent is taken to provide service and offerings. There’s also zero advertising on our app, so there are never any pesky promotions from others.</li><br>
</ul>
</p>
