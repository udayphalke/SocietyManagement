   <h4 class="serif">&nbsp;&nbsp;&nbsp;(1) Lawfulness, fairness and transparency: </h4>
  <p>Ensuring completely transparent and legally compliant information collection at all levels.
</p>

<h4 class="serif">&nbsp;&nbsp;&nbsp;(2) Purpose limitation:</h4>
<p>Ensuring that information collection is purpose oriented, clearly defined and maintained only for as long as it is necessary to fulfil the purpose.
</p>

<h4 class="serif">&nbsp;&nbsp;&nbsp;(3) Data minimisation: </h4>
<p>Ensuring that information is cautiously collected and handled, and that information deemed unnecessary for the stated purpose is not collected at all.
  </p>

<h4 class="serif">&nbsp;&nbsp;&nbsp;(4) Storage limitation: </h4>
<p>Ensuring that personal information that is no longer necessary is deleted upon fulfilment of the purpose.
  </p>
